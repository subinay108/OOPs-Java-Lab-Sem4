// WAP in Java to calculate the area and circumference of a circle
public class Program8{
    public static void main(String[] args){
        double radius = 3.2;
        System.out.printf("Area : %.2f\n", 3.14159 * radius * radius);
        System.out.printf("Circumference: %.2f", 2 * 3.14159 * radius);
    }
}